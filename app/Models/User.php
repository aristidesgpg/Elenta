{
    use HasApiTokens;
    use Notifiable;
    use SoftDeletes;
    use Billable;
    use UsesUuid;

    protected $fillable = [
        'name', 'email', 'password',
    ];

    protected $hidden = [
        'password', 'remember_token',
    ];

    protected $casts = [
        'email_verified_at' => 'datetime',
    ];

    public function learner(): HasMany {
        return $this->hasMany(LearnerProfile::class);
    }

    public function consultant(): HasMany {
        return $this->hasMany(ConsultantProfile::class);
    }
